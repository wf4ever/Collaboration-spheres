package rdf;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class Output {
	
	private String inner;
	private String user;
	
	public String getInner() {
		return inner;
	}
	public void setInner(String inner) {
		this.inner = inner;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	


}
